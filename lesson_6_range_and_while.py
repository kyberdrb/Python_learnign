# for loop from 0 to 9
for x in range(10):
    print("Bucky is awesome")

print()

# for loop in interval: start at 5, end at 11 (12 - 1)
for x in range(5, 12):
    print(x)

print()

# for loop in interval: start at 10, end at 35 with a step 5
for x in range(10, 40, 5):
    print(x)

print()

# while loop
buttcrack = 5

while buttcrack < 10:
    print(buttcrack)
    buttcrack += 1